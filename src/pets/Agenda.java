package pets;

public class Agenda {

	private String data;
	private String hora;
	private String evento;
	
	// gets e sets de data.
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	// gets e sets de hora.
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	
	// gets e sets de evento.
	public String getEvento() {
		return evento;
	}
	public void setEvento(String evento) {
		this.evento = evento;
	}
	
}
